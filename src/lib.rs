#[test]
fn blowfish_test_enctypt_decrypt() {
    let mut ctx: &mut blowfish_header::mbedtls_blowfish_context =
        &mut blowfish_header::mbedtls_blowfish_context {
            P: blowfishc::P,
            S: blowfishc::S,
        };

    let mut xl: u32 = 0x3AC372E6;
    let original_xl: u32 = xl.clone();
    let mut xr: u32 = 0xCE77E25B;
    let original_xr = xr.clone();
    let enc_xl_answer: u32 = 0xbe984ec2;
    let enc_xr_answer: u32 = 0xfb84409c;

    blowfishc::blowfish_enc(&mut ctx, &mut xl, &mut xr);
    assert_eq!(xl, enc_xl_answer);
    assert_eq!(xr, enc_xr_answer);

    blowfishc::blowfish_dec(&mut ctx, &mut xl, &mut xr);
    assert_eq!(xl, original_xl);
    assert_eq!(xr, original_xr);
}

#[test]
fn blowfish_test_ecb() {
    let mut ctx: &mut blowfish_header::mbedtls_blowfish_context =
        &mut blowfish_header::mbedtls_blowfish_context {
            P: blowfishc::P,
            S: blowfishc::S,
        };

    blowfishc::mbedtls_blowfish_init(&mut ctx);
    blowfishc::mbedtls_blowfish_setkey(&mut ctx, "10010110001110101001011000111010", 32);
    let mut b: [u8; 8] = [49, 50, 51, 52, 53, 54, 55, 56];
    let original_b = b.clone();
    let enc_answer: [u8; 8] = [201, 253, 82, 207, 202, 206, 145, 97];
    let mut c: [u8; 8] = [0, 0, 0, 0, 0, 0, 0, 0];
    let mut d: [u8; 8] = [0; 8];

    blowfishc::mbedtls_blowfish_crypt_ecb(
        &mut ctx,
        blowfish_header::MBEDTLS_BLOWFISH_ENCRYPT,
        b,
        &mut c,
    );
    assert_eq!(c, enc_answer);

    blowfishc::mbedtls_blowfish_crypt_ecb(
        &mut ctx,
        blowfish_header::MBEDTLS_BLOWFISH_DECRYPT,
        c,
        &mut d,
    );
    assert_eq!(d, original_b);
}

