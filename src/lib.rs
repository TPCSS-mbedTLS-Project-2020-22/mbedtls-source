#[test]
fn blowfish_test_ctr() {
    let mut ctx: &mut blowfish_header::mbedtls_blowfish_context =
        &mut blowfish_header::mbedtls_blowfish_context {
            P: cipher::blowfishc::P,
            S: cipher::blowfishc::S,
        };
    cipher::blowfishc::mbedtls_blowfish_setkey(&mut ctx, "10010110001110101001011000111010", 32);
    let mut nonc: [char; blowfish_header::MBEDTLS_BLOWFISH_BLOCKSIZE] =
        ['0', '2', '3', '8', '5', '6', '7', '8'];
    let mut iv: [char; blowfish_header::MBEDTLS_BLOWFISH_BLOCKSIZE] =
        ['1', '2', '3', '4', '5', '6', '7', '8'];
    let mut k3 = String::from("ijklmnopijklmnopabcdefgh[]||tuvw");
    let original_str = String::from("abcdefg@*&rstuvwabcdefgh[]||tuvw");
    let mut noff: usize = 0;

    println!("========blowfish ctr  test =========");
    blowfishc::mbedtls_blowfish_crypt_ctr(
        &mut ctx,
        32,
        &mut noff,
        &mut nonc,
        &mut iv,
        String::from("abcdefg@*&rstuvwabcdefgh[]||tuvw"),
        &mut k3,
    );
    println!("{:?} {:?}", k3, nonc);
    let mut k4 = String::from("ijklmnopijklmnopabcdefgh[]||tuvw");
    noff = 0;
    nonc = ['0', '2', '3', '8', '5', '6', '7', '8'];
    blowfishc::mbedtls_blowfish_crypt_ctr(&mut ctx, 32, &mut noff, &mut nonc, &mut iv, k3, &mut k4);
    println!("{:?} {:?}", k4, nonc);
    assert_eq!(original_str, k4);
}
